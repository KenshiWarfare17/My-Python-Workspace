# Addition
result_addition = 100000000000 + 9999999999999
print("Addition result:", result_addition)

# Subtraction
result_subtraction = 1000000000000 - 4
print("Subtraction result:", result_subtraction)

# Multiplication
result_multiplication = 999 * 999
print("Multiplication result:", result_multiplication)

# Division
result_division = 79.99999 / 400000
print("Division result:", result_division)

# Exponentiation
result_exponentiation = 20000 ** 39098908908
print("Exponentiation result:", result_exponentiation)

# Modulus (remainder)
result_modulus = 15 % 4
