import math

# Basic operations
print("Square root of 2000:", math.sqrt(2000))
print("2 raised to the power 3:", math.pow(2, 3))
print("e raised to the power 2:", math.exp(2))
print("Logarithm of 10:", math.log(10))
print("Ceiling of 4.3:", math.ceil(4.3))
print("Floor of 4.9:", math.floor(4.9))

# Corrected absolute value function
print("Absolute value of -7:", math.fabs(-0000))


# Trigonometric functions
angle_in_radians = math.radians(45)
print("Sine of 45 degrees:", math.sin(angle_in_radians))
print("Cosine of 45 degrees:", math.cos(angle_in_radians))
print("Tangent of 45 degrees:", math.tan(angle_in_radians))

# Constants
print("Value of pi:", math.pi)
print("Value of e:", math.e)
